function [firstText, secondText, selectedOption, doPractice] = experiment_pre_popup(dropdownOptions)
% previous function name: prePopUp()
% Create a small pop-up window
% Create a single dialog with two text inputs and a dropdown menu

% Define dropdown options (replace with your list of values)
% dropdownOptions = {'Option1', 'Option2', 'Option3', 'Option4'};

boxWidth = 320;
boxHeight = 220;
padding=30;

% Create dialog figure
fig = figure('Name', 'Input Dialog', ...
    'Position', [600, 300, boxWidth, boxHeight+30], ...
    'MenuBar', 'none', ...
    'NumberTitle', 'off', ...
    'Resize', 'off', ...
    'WindowStyle', 'modal', ...
    'CloseRequestFcn', @cancelCallback);

% Text input 1
uicontrol('Style', 'text', ...
          'Position', [20, boxHeight-padding, 100, 20], ...
          'String', 'Participant number:', ...
          'HorizontalAlignment', 'left');
edit1 = uicontrol('Style', 'edit', ...
                  'Position', [120, boxHeight-padding, 180, 20], ...
                  'String', '000', 'Max', 1);

% Text input 2
uicontrol('Style', 'text', ...
          'Position', [20, boxHeight-padding*2, 100, 20], ...
          'String', 'Note:', ...
          'HorizontalAlignment', 'left');
edit2 = uicontrol('Style', 'edit', 'Position', [120, boxHeight-padding*2, 180, 20], ...
                  'String', '', 'Max', 1);

% Dropdown menu
uicontrol('Style', 'text', ...
          'Position', [20, boxHeight-padding*3, 100, 20], ...
          'String', 'Select task:', ...
          'HorizontalAlignment', 'left');
popup = uicontrol('Style', 'popupmenu', ...
                  'Position', [120, boxHeight-padding*3, 180, 20], ...
                  'String', dropdownOptions, ...
                  'Value', 1);

% Checkbox: Should we skip practice?
uicontrol('Style', 'text', ...
          'Position', [20, boxHeight-padding*4-10, 100, 20], ...
          'String', 'Skip practice?', ...
          'HorizontalAlignment', 'left');
checkboxPractice = uicontrol('Style', 'checkbox', ...
                             'Position', [120, boxHeight-padding*4-10, 100, 20], ...
                             'String', '', ...
                             'HorizontalAlignment', 'left', ...
                             'Value', 0);

% Cancel button
uicontrol('Style', 'pushbutton', ...
          'Position', [floor(boxWidth/2)-100, boxHeight-padding*5-30, 80, 28], ...
          'String', 'Cancel', ...
          'Callback', @cancelCallback);

% OK button
uicontrol('Style', 'pushbutton', ...
          'Position', [floor(boxWidth/2)+26, boxHeight-padding*5-30, 80, 28], ...
          'String', 'Start', ...
          'Callback', @okCallback);

% Store data
fig.UserData = struct('edit1', edit1, ...
                      'edit2', edit2, ...
                      'popup', popup, ...
                      'practice', checkboxPractice, ...
                      'options', {dropdownOptions}, ...
                      'result', [], ...
                      'canceled', true);

% Wait for dialog to close
uiwait(fig);

% Retrieve results
if ishandle(fig) && ~fig.UserData.canceled
    result = fig.UserData.result;
    firstText = result.firstText;
    secondText = result.secondText;
    selectedOption = result.selectedOption;
    doPractice = result.practice;
else
    firstText = '';
    secondText = '';
    selectedOption = '';
    doPractice = 1;
end

% Clean up
if ishandle(fig)
    delete(fig);
end

% OK button callback
function okCallback(~, ~, ~)
    data = fig.UserData;
    
    % Get inputs
    firstText = strtrim(get(data.edit1, 'String'));
    secondText = strtrim(get(data.edit2, 'String'));
    selectedOption = data.options{get(data.popup, 'Value')};
    doPractice = get(data.practice, 'Value');
    fprintf('doPractice:%s\n', doPractice);
    
    % Validate text inputs
    if length(firstText) > 20
        errordlg('First text exceeds 20 characters!', 'Input Error');
        return;
    end
    if length(secondText) > 50
        errordlg('Second text exceeds 50 characters!', 'Input Error');
        return;
    end
    
    % Store results
    fig.UserData.result = struct('firstText', firstText, ...
                                 'secondText', secondText, ...
                                 'selectedOption', selectedOption, ...
                                 'practice', doPractice);
    fig.UserData.canceled = false;
    
    % Close dialog
    uiresume(fig);
end

% Cancel button and window close callback
function cancelCallback(~, ~, ~)
    fig.UserData.canceled = true;
    uiresume(fig);
end

end
